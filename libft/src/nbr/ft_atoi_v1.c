#include "../../inc/nbr.h"

long	ft_atoi_v1(const char *str)
{
	char	*s;
	long	signe;

	s = (char *)str;
	while (*s && ((*s < 33 && *s != 27) && *s != '\200'))
		s++;
	signe = 1;
	if (*s == '-' || *s == '+')
	{
		signe = (*s == '-') ? -1 : 1;
		s++;
	}
	return ((*s) ? (long)signe * ft_base_to_decimal(s, DEC) : 0);
}
