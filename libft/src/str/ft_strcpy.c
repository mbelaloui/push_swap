#include "../../inc/str.h"

char	*ft_strcpy(char *dst, const char *src)
{
	size_t	pt;

	pt = 0;
	while (src[pt])
	{
		dst[pt] = src[pt];
		pt++;
	}
	dst[pt] = '\0';
	return (dst);
}
